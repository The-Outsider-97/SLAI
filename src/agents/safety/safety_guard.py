@dataclass(frozen=True)
class GuardPattern:
    """Compiled safety pattern metadata owned by secure_config.yaml/resources."""

    name: str
    pattern: str
    category: str
    replacement: Optional[str] = None
    severity: str = "medium"
    weight: float = 1.0
    description: str = ""
    source: str = "config"
    flags: Tuple[str, ...] = ()
    compiled: re.Pattern[str] = field(default=None, repr=False, compare=False)  # type: ignore[assignment]

    def to_dict(self) -> Dict[str, Any]:
        data = asdict(self)
        data.pop("compiled", None)
        data["pattern_fingerprint"] = fingerprint(self.pattern)
        data.pop("pattern", None)
        return redact_value(data)


@dataclass(frozen=True)
class GuardFinding:
    """Audit-safe finding generated by SafetyGuard."""

    finding_id: str
    category: str
    severity: str
    risk_score: float
    confidence: float
    action: str
    pattern_name: str
    pattern_fingerprint: str
    evidence_preview: str
    remediation: str = ""
    metadata: Dict[str, Any] = field(default_factory=dict)

    def to_dict(self) -> Dict[str, Any]:
        return {
            "finding_id": self.finding_id,
            "category": self.category,
            "severity": self.severity,
            "risk_score": clamp_score(self.risk_score),
            "confidence": clamp_score(self.confidence),
            "action": self.action,
            "pattern_name": self.pattern_name,
            "pattern_fingerprint": self.pattern_fingerprint,
            "evidence_preview": redact_text(self.evidence_preview, max_length=256),
            "remediation": redact_text(self.remediation, max_length=512),
            "metadata": redact_value(self.metadata),
        }


@dataclass(frozen=True)
class SafetyAnalysis:
    """Structured safety analysis result suitable for audit pipelines."""

    schema_version: str
    module_version: str
    analysis_id: str
    text_fingerprint: str
    sanitized_text: str
    changed: bool
    depth: str
    decision: str
    risk_score: float
    risk_level: str
    findings: List[GuardFinding]
    protection_stack: List[str]
    privacy_config: Dict[str, Any]
    context_fingerprint: Optional[str] = None
    metadata: Dict[str, Any] = field(default_factory=dict)
    timestamp: str = field(default_factory=utc_iso)

    def to_dict(self, *, include_sanitized_text: bool = True) -> Dict[str, Any]:
        data = {
            "schema_version": self.schema_version,
            "module_version": self.module_version,
            "analysis_id": self.analysis_id,
            "text_fingerprint": self.text_fingerprint,
            "changed": self.changed,
            "depth": self.depth,
            "decision": self.decision,
            "risk_score": clamp_score(self.risk_score),
            "risk_level": self.risk_level,
            "findings": [finding.to_dict() for finding in self.findings],
            "protection_stack": redact_value(self.protection_stack),
            "privacy_config": redact_value(self.privacy_config),
            "context_fingerprint": self.context_fingerprint,
            "metadata": redact_value(self.metadata),
            "timestamp": self.timestamp,
        }
        if include_sanitized_text:
            data["sanitized_text"] = redact_text(self.sanitized_text, max_length=4096)
        return data


class SafetyGuard:
    """
    Unified privacy and safety framework with layered protection.

    Legacy public methods are retained:
    - sanitize(text, depth='full') -> str
    - get_protection_report(text) -> dict
    - is_compliant(text, standard='gdpr') -> bool
    - apply_differential_privacy(value, dataset_size=1) -> float
    - detect_prompt_injection(prompt) -> bool
    - detect_misinformation(content) -> bool
    - is_minimal_viable() -> bool
    """

    monitor_thread = True

    def __init__(self, privacy_params: Optional[Dict[str, Any]] = None):
        self.config = load_global_config()
        self.complience_config = get_config_section("safety_guard")
        self.guard_config = self.complience_config
        self.memory = SecureMemory()

        self.enabled = coerce_bool(self.guard_config.get("enabled", True), True)
        self.strict_config_validation = coerce_bool(self.guard_config.get("strict_config_validation", True), True)
        self.max_text_length = coerce_int(self.guard_config.get("max_text_length"), 8192, minimum=1)
        self.max_context_length = coerce_int(self.guard_config.get("max_context_length"), 16384, minimum=1)
        self.store_events = coerce_bool(self.guard_config.get("store_events", True), True)
        self.raise_on_block = coerce_bool(self.guard_config.get("raise_on_block", True), True)
        self.return_block_marker = str(self.guard_config.get("block_marker", "[SAFETY_BLOCK] Content violates safety policy"))
        self.allowed_depths = set(str(item) for item in self.guard_config.get("allowed_depths", ["minimal", "balanced", "full"]))
        self.context_window = coerce_int(self.guard_config.get("context_window", 8), 8, minimum=1)
        self._conversation_history: "deque[Dict[str, str]]" = deque(maxlen=self.context_window)

        self.epsilon = coerce_float(self.guard_config.get("epsilon"), 1.0, minimum=0.000001)
        self.sensitivity = coerce_float(self.guard_config.get("sensitivity"), 1.0, minimum=0.0)
        self.mechanism = str(self.guard_config.get("mechanism", "laplace")).lower().strip()
        self.delta = coerce_float(self.guard_config.get("delta"), 1e-5, minimum=1e-12, maximum=1.0)
        self.min_dataset_size = coerce_int(self.guard_config.get("min_dataset_size"), 1, minimum=1)
        seed = self.guard_config.get("privacy_noise_seed")
        self._rng = np.random.default_rng(coerce_int(seed, 0) if seed is not None else None)

        self.identity_markers = [str(item).lower() for item in self.guard_config.get("identity_markers_for_harassment", [])]
        self.privacy_params = {
            "epsilon": self.epsilon,
            "sensitivity": self.sensitivity,
            "mechanism": self.mechanism,
            "delta": self.delta,
        }
        if privacy_params:
            self.privacy_params.update(redact_value(dict(privacy_params)))
            self.epsilon = coerce_float(self.privacy_params.get("epsilon"), self.epsilon, minimum=0.000001)
            self.sensitivity = coerce_float(self.privacy_params.get("sensitivity"), self.sensitivity, minimum=0.0)
            self.mechanism = str(self.privacy_params.get("mechanism", self.mechanism)).lower().strip()
            self.delta = coerce_float(self.privacy_params.get("delta"), self.delta, minimum=1e-12, maximum=1.0)

        self._validate_configuration()
        self._validate_privacy_params()

        self.redact_patterns = self._load_pii_patterns()
        self.toxicity_patterns = self._load_toxicity_patterns()
        self.authority_phrases = self._load_authority_phrases()
        self.manipulation_patterns = self._load_manipulation_patterns()
        self.group_targeting_patterns = self._load_group_targeting_patterns()
        self.boundary_phrases = self._load_boundary_phrases()
        self.injection_patterns = self._load_prompt_injection_patterns()
        self.misinformation_patterns = self._load_misinformation_patterns()
        self.sensitive_contexts = self._load_sensitive_contexts()
        self.conversation_risk_patterns = self._load_conversation_risk_patterns()

        self._compiled_redaction = self._compile_guard_patterns(self.redact_patterns, default_category="pii")
        self._compiled_toxicity = self._compile_guard_patterns(self.toxicity_patterns, default_category="toxicity")
        self._compiled_authority = self._compile_guard_patterns(self.authority_phrases, default_category="authority_escalation")
        self._compiled_manipulation = self._compile_guard_patterns(self.manipulation_patterns, default_category="prompt_manipulation")
        self._compiled_group_targeting = self._compile_guard_patterns(self.group_targeting_patterns, default_category="targeted_harassment")
        self._compiled_boundary = self._compile_guard_patterns(self.boundary_phrases, default_category="boundary_testing")
        self._compiled_injection = self._compile_guard_patterns(self.injection_patterns, default_category="prompt_injection")
        self._compiled_misinformation = self._compile_guard_patterns(self.misinformation_patterns, default_category="misinformation")
        self._compiled_conversation_risk = self._compile_guard_patterns(self.conversation_risk_patterns, default_category="conversation_risk")

        logger.info(
            "Safety Guard initialized: %s",
            safe_log_payload(
                "safety_guard_initialized",
                {
                    "schema_version": self.guard_config.get("schema_version"),
                    "enabled": self.enabled,
                    "pii_patterns": len(self._compiled_redaction),
                    "toxicity_patterns": len(self._compiled_toxicity),
                    "prompt_security_patterns": len(self._compiled_injection) + len(self._compiled_manipulation),
                },
            ),
        )

