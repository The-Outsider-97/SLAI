"""
Production-grade Safety Guard for the SLAI Safety Agent Framework.

This module is the runtime safety/privacy gate for user-facing and internal
content. It performs layered sanitization, PII protection, prompt-security
checks, toxicity and harassment screening, conversational risk analysis,
compliance checks, and differential-privacy noise injection.

Design goals:
- keep pattern and threshold ownership in secure_config.yaml;
- avoid leaking raw prompts, PII, secrets, or unsafe content into logs, memory,
  exception messages, or reports;
- use shared safety_helpers for redaction, normalization, hashing, path-safe
  file loading, timestamps, score normalization, decisions, and logging;
- use structured security_error exceptions for fail-closed behavior;
- preserve the legacy public API used by the current safety subsystem;
- keep this module focused on guard decisions rather than reward scoring,
  model training, intervention orchestration, or long-term memory policy.
"""

